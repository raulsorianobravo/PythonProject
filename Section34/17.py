
# List Concatenation
# Implement a function that takes three lists as parameters and returns one concatenated list.

# For example, if I called your function like foo([1, 2], [5, 6, 7], [10, 11]) it should return [1, 2, 5, 6, 7, 10, 11]

def foo(lst1, lst2, lst3):
    return lst1+lst2+lst3

print(foo([1, 2], [5, 6, 7], [10, 11]))
